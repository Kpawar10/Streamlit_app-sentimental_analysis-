import subprocess
import time
from pyngrok import ngrok

# Step 1: Launch Streamlit app
streamlit_process = subprocess.Popen(["streamlit", "run", "app.py"])

# Wait for a few seconds to ensure Streamlit has started
time.sleep(5)

# Step 2: Create a public URL with ngrok
public_url = ngrok.connect(8501)
print(f"\nüåê Your app is live at: {public_url}\n")

try:
    # Keep script running to maintain ngrok tunnel
    streamlit_process.wait()
except KeyboardInterrupt:
    print("\n‚ùå Shutting down...")
    streamlit_process.terminate()
    ngrok.disconnect(public_url)

